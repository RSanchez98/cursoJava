public class PrimitivosFloat {
    public static void main(String[] args) {
        float realFloat1 =2120f;
        float realFloat2 =2.12e3f;

        System.out.println("realFloat1 = " + realFloat1);
        System.out.println("realFloat2 = " + realFloat2);

        System.out.println("Float en bytes = " + Float.BYTES);
        System.out.println("Float en bites = " + Float.SIZE);
        System.out.println("Float maximo = " + Float.MAX_VALUE);
        System.out.println("Float minimo = " + Float.MIN_VALUE);

        double realDouble = 3.5465450E54;
        System.out.println("realDouble = " + realDouble);
    }
}
